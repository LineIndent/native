"""
Turns a --TOKEN(name)-- delimited markdown file into a list of rx.Component.

Two registries do two different jobs, and neither should be confused for
the other:

  COMPONENT_REGISTRY  (app/registry/components.py, autogenerated)
      name -> {"files": [...], "dependencies": [...]}
      Static file paths + install-time deps. Only used by --INSTALL(...)--.

  _LIVE_REGISTRY (built once, right here, at import time)
      name -> (object, original_name)
      The actual importable function/class/ComponentNamespace from
      examples/, blocks/, getting_started/ and components/ui/, keyed by
      *its own name*, not the filename (they don't always match). Used by
      --DEMO / --CODE / --USAGE / --ANATOMY(...)--, since those need a
      real object to call and/or introspect for source.
"""

import importlib
